import { writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { build } from 'esbuild';

// Bundle redirects.ts to a data URL so we can dynamic-import it from a .mjs
// without needing a runtime TS loader.
const PROJECT_ROOT = join(import.meta.dirname, '..');
const SOURCE = join(PROJECT_ROOT, '.vitepress/redirects.ts');
const OUT_PATH = join(PROJECT_ROOT, 'nginx-redirects.conf');

const bundle = await build({
  entryPoints: [SOURCE],
  bundle: true,
  format: 'esm',
  platform: 'node',
  target: 'node20',
  write: false,
});
const code = bundle.outputFiles[0].text;
const dataUrl = `data:text/javascript;base64,${Buffer.from(code).toString('base64')}`;
const { REDIRECTS } = await import(dataUrl);

// Group rules into exact and prefix buckets so the generated config has the
// shape: one `map` block for exact matches + per-prefix `location` blocks.
const exacts = REDIRECTS.filter((r) => r.kind === 'exact');
const prefixes = REDIRECTS.filter((r) => r.kind === 'prefix');

const mapLines = exacts.flatMap((rule) => {
  // Match both clean-URL and .html variants in one rule. `clean form .html`
  // redirects the old .html path to the clean form on the NEW page.
  const paths = [rule.from, `${rule.from}.html`];
  if (rule.code === 410) {
    return paths.map((p) => `    "${p}"  "410";`);
  }
  return paths.map((p) => `    "${p}"  "${rule.to}";`);
});

const locationBlocks = prefixes.map((rule) => {
  if (rule.code === 410) {
    return [
      `# ${rule.prefix}** -> 410 Gone`,
      `location ^~ ${rule.prefix} {`,
      `    return 410;`,
      `}`,
    ].join('\n');
  }
  return [
    `# ${rule.prefix}** -> ${rule.to} (301)`,
    `location ^~ ${rule.prefix} {`,
    `    return 301 ${rule.to};`,
    `}`,
  ].join('\n');
});

// The outer nginx `map` directive must live at http{} scope. The nginx main
// config at /etc/nginx/nginx.conf includes /etc/nginx/conf.d/*.conf inside
// http{}, so a top-level map here lands in the right place. The `location`
// blocks must live at server{} scope, so we emit them in a separate file
// that gets `include`d inside the server{} block of default.conf.
const mapConf = `# Generated by scripts/build-nginx-redirects.mjs — edit .vitepress/redirects.ts instead.
map $request_uri $torale_redirect {
    default "";
${mapLines.join('\n')}
}
`;

const locationConf = `# Generated by scripts/build-nginx-redirects.mjs — edit .vitepress/redirects.ts instead.
# Exact-match redirects via the $torale_redirect map (populated in /etc/nginx/conf.d/nginx-redirects-map.conf).
if ($torale_redirect = "410") { return 410; }
if ($torale_redirect != "") { return 301 $torale_redirect; }

${locationBlocks.join('\n\n')}
`;

writeFileSync(join(PROJECT_ROOT, 'nginx-redirects-map.conf'), mapConf, 'utf-8');
writeFileSync(OUT_PATH, locationConf, 'utf-8');
console.log(`Wrote ${exacts.length} exact + ${prefixes.length} prefix rules to:`);
console.log(`  ${OUT_PATH}`);
console.log(`  ${join(PROJECT_ROOT, 'nginx-redirects-map.conf')}`);
